			<h1>Shaft Tool</h1>
			<p>
				<b>This function allows a feature(s)/geometry to be repeated in a rectangular pattern.</b>
				<br>
				In this function there are 4 parameters that the user can select. They are:</b>
				<br></br>
				<br></br>
				<b>Instance(s) & Length</b>
				<br>
				In this parameter mode, the user enters the number of instances of the feature to repeat and the length, which is the distance between the first and the last feature.
				<br></br>
				<b>Instance(s) & Spacing</b>
				<br>
				In this parameter mode, the user enters the number of instances of the feature to repeat and the spacing between each feature.
				<br></br>
				<b>Spacing & Length</b>
				<br>
				In this parameter mode, the user enters the spacing between each feature and the length between the first and last feature.
				<br></br>
				<b>Instances(s) & Unequal Spacing</b>
				<br>
				In this parameter mode, the user enters the number of instances of the feature to repeat but is also able to enter the individual spacing between each features.
				<br></br>
				<br></br>
				To use this feature
				<br>
				1.  To use this function, first select the feature/geometry that needs to be replicated.
				<img class="tools_image" src="Tools\Tools_Images\Rectangle Pattern\rp1.png" ;>
				<br>
				Next, Click on the rectangular pattern icon
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Rectangle Pattern\rp2.png" ;>
				<br>
				Now select the reference direction for the first and second direction
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Rectangle Pattern\rp3.png" ;>
				<br>
				Finally, choose they type of parameter that you would like to work on and enter the values into the box for both direction
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Rectangle Pattern\rp4.png" ;>
			</p>
