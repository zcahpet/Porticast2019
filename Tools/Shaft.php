			<h1>Shaft Tool</h1>
			<p>
				<br>
				For this short tutorial we will be only be using the part design workbench to make a simple bowl shape, which will hopefully demonstrate how the shaft tool can be used.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Shaft\shaft1.png" ;>
				<br></br>
				If you haven’t already opened it, go to the <b>Start</b> menu and select <b>Mechanical Design</b>, followed by <b>Part Design</b>. Now go straight into the <b>sketch</b> bench; the plane used doesn’t matter but for this tutorial, let’s use the yz plane. Now we will sketch one half of the bowl shaped desired; whenever you want to use the shaft tool it is important to note that you do not need to draw an entire outline as the sketch will be revolved to form the complete shape.
				<br>
				We will start by forming the simplified version of the outside edge of the shape first as shown below (dimensions are also shown in the image). Don’t worry that we are using entirely straight edges; this will be addressed later.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Shaft\shaft2.png" ;>
				<br></br>
				Next we will form the inside edge of the bowl shape using parallelism and the various dimensions as shown below. Don’t forget to close the shape off at the ends.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Shaft\shaft3.png" ;>
				<br></br>
				Now you can exit the <b>sketch</b> bench. Find the <b>shaft</b> tool on your workbench and click on it; the profile used will be the sketch you have just created, with the axis being the <b>vertical direction</b> line that should be present at the origin. Alternatively, you could draw a line in another sketch to be used as an axis but in this situation that is unnecessary. Once you have selected these options, ensuring that the first angle limit is 360 degrees, click ok. You should now find that you have created a complete bowl shape as shown below.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Shaft\shaft4.png" ;>
				<br></br>
				If you wish, you can approve the aesthetics using the <b>edge fillet</b> tool. As well as serving a visual effect, the edge fillet tool can be used for strengthening purposes as well; applying a fillet at a corner will reduce the stress concentrations found there for many applications. Once you have applied the fillets shown below, you have your bowl! The shaft tool can be applied to many more important applications so have a go at experimenting with your own shapes.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Shaft\shaft5.png" ;>
			</p>
