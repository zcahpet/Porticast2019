			<h1>Wall Thickness Tool</h1>
				<b>The Wall Thickness Analysis tool is used to investigate the change in thickness of a part.</b>
				<p>Firstly, select the tool from the “Analysis” toolbar. It looks like this;</p>
				<img class="tools_image" src="Tools\Tools_Images\Wall\Wall-icon.gif">
				<p>Then select the part to be analysed, the method of analysis and then the precision. Press run and wait for the program to do its work.</p>
				<img class="tools_image" src="Tools\Tools_Images\Wall\Wall-1.png">
				<p>Your object will now appear with a kaleidoscope of colours that relate to the thickness of the part.</p>
				<img class="tools_image" src="Tools\Tools_Images\Wall\Wall-2.png">
				<p>To change the way the analysis is viewed, go to Options->Display->Sphere/Ray</p>
				<img class="tools_image" src="Tools\Tools_Images\Wall\Wall-3.png">
				<p>Now when you hover over the object, spheres/rays will appear on screen, indicating the thickness along various parts of the body</p>
