			<p>
			<b>
				The Remove tool is used to remove the regions where multiple bodies intersect.
			</b>
				<br>
				Click on the Remove tool.
				<br>
				<img style="padding: 10px 0px;" src="Tools/Tools_Images/Remove/remove_icon.png">
				<br>
				A pop up box will again appear. Select the body you would like to remove as the ‘Remove’ part, and the body you would it to be removed from as the ‘From’ part.
				<br>
				<img style="padding: 10px 0px;" src="Tools/Tools_Images/Remove/remove_parts.png">
				<br>
				Click OK. The region where the objects intersect should be removed along with the body you chose to remove, leaving behind a cube with a hemispherical shape missing, as seen in the image below. You will also notice that the bodies have merged into 1 Part Body in the tree, and “Remove” appears on a branch of the tree.
