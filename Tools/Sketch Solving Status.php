			<h1>Split Tool</h1>
			<p>
				For this tutorial we will be teaching you how to use the sketch solving status, or the traffic light.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\SSS\sss1.png" ;>
				<br>
				This can be found on the “Tools” toolbar and is the traffic light button as shown.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\SSS\sss2.png" ;>
				<br>
				The sketch solving status shows the status of your sketch, and it also allows you to further analyse the problem on the sketch by clicking the sketch analysis button.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\SSS\sss3.png" ;>
				<br>
				The highlighted parts are errors on your sketch, and you can solve the problem by using the analysis to add or remove constraints and geometry
				<br>
				<img class="tools_image" src="Tools\Tools_Images\SSS\sss4.png" ;>
				<br>
				This function is very useful if you have a sketch that will not pad or pocket because of an error. If you click on the Geometry tab, the diagnostics will tell you where the problem lies. This is typically if there is an open gap in the sketch profile or if the geometry has not been trimmed properly.
