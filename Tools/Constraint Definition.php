			<h1>Constraints Definition Tool</h1>
			<p>
				There are many constraints that are available to use on your sketch, each with different functions.
				Constraints are imposed onto sketches to ensure that the profile does not change when one of its dimensions is altered.
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_box.png">
				<br>
				Distance – the offset between 2 geometries:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_distance.png">
				<br>
				Length – determines the length of specific geometry:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_length.png">
				<br>
				Angle – the angle between 2 selected elements:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_angle.png">
				<br>
				Radius/Diameter – sets the radius and/or diameter of a circle/arc:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_radius.png">
				<br>
				Semimajor axis – dimensions the length of the major axis of an ellipse:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_semimajor.png">
				<br>
				Semiminor axis – dimensions the length of the minor axis of an ellipse:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_semiminor.png">
				<br>
				Symmetry – makes sure the object is symmetrical about an axis selected. You have to select the 2 objects that will be in symmetry with one another first then select the axis of symmetry:
				<br> 
				(Remember to hold the Ctrl key!)
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_symmetry.png">
				<br>
				Midpoint – constrains an existing point to the centre of a line:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_midpoint.png">
				<br>
				Equidistant Point – moves an existing point to a position where it is the same point from existing geometry: 
				<br>
				(The order in which the geometry is selected is important; first the two lines have two be selected before the point.)
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_equidistant.png">
				<br>
				Fix – prevents the selected geometry from translating:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_fix.png">
				<br>
				Coincidence – moves the objects so that they are in line:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_coincidence.png">
				<br>
				Concentricity – moves the point to the centre of the circle:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_concentricity.png">
				<br>
				Tangency – moves lines to make them tangential to existing geometry:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_tangency.png">
				<br>
				Parallelism – makes two lines parallel:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_parallelism.png">
				<br>
				Perpendicular – makes two lines perpendicular:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_perpendicular.png">
				<br>
				Horizontal – Creates a line that is horizontal:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_horizontal.png">
				<br>
				Vertical – Creates a line that is vertical:
				<br>
				<img class="tools_image" src="Tools/Tools_Images/Constraints/constraints_vertical.png">
			</p>
