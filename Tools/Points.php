			<h1>Points Tool</h1>
			<p>
				<b>Points are reference elements that are used to create a line, axis system and complex shapes</b>
				<br></br>
				The Point icon can be found in the Reference Element toolbar</b>
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Points\points1.png" ;>
				<br>
				After clicking the icon you are presented with Point Definition box.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Points\points2.png" ;>
				<br></br>
				By clicking the green padlock symbol, it allows us to prevent an automatic change of point type while selecting a geometry. Simply clicking changes its colour to red and locks the point type.
				<br>
				The user can choose the point type in the point definition box.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Points\points3.png" ;>
				<br></br>
				There are several ways to construct a point:
				<br>
				<ul>
					<li>Coordinates</li>
					<li>On Curve</li>
					<li>On Plane</li>
					<li>On Surface</li>
					<li>Circle/Sphere/Ellipse centre</li>
					<li>Tangent on curve</li>
					<li>Between</li>
				</ul>
				<br></br>
				<h3>Example â€“ Coordinate</h3>
				<br>
				In this mode, the user can construct a point by specifying the X, Y, Z coordinates.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Points\points4.png" ;>
				<br></br>
				Before specifying the X, Y, Z coordinate, remember to select your reference point and axis system. Failure to do so might result in points being constructed in the wrong place. On most occasions, the Default Origin point and Absolute Axis System are used.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Points\points5.png" ;>
				<br></br>
				Finally, specify the coordinate and click ok to construct it.
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Points\points6.png" ;>
