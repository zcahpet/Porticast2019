			<h1>Thickness Tool</h1>
			<p>
				<b>The thickness tool adds or removes material to or from a face, in order to increase order to decrease its size.</b>
				<br></br>
				It can be used as a quick way to change the size of a local feature on a solid body without having to first create a sketch. It is also used to extend the size of the solid with complex profiles, such as a spline or surface, so it can follow the shape.
				<br></br>
				The thickness tool can be found under the “Dress-Up Features” toolbar, shown below;
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Thickness\thickness_1.png" ;>
				<br></br>
				Before using the tool, make sure you have a suitable part already loaded;
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Thickness\thickness_2.png" ;>
				<br></br>
				Then select the tool; you should see the following dialogue box;
				<br>
				<img class="tools_image" src="Tools\Tools_Images\Thickness\thickness_3.png" ;>
				<br></br>
				Here, you can enter the desired thickness for a surface of your choice. You will have to select this surface using the dialogue box. Also, you have to select the surfaces where this thickness will be added. A preview of the effect can be seen before clicking ok.
			</p>
