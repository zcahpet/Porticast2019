			<h1>Groove Tool</h1>
			<p>
			<b>
				The Groove tool is used to remove material from a solid feature of the part’s body.
			</b>
				<br>
				<br>
				In order to use the groove tool, you must first create a sketch that can be used to remove material from the part. It doesn’t matter what shape this sketch is, as long as it is a closed profile.:
				<br>
				<img style="padding: 10px 0px;" src="Tools/Tools_Images/Groove/groove_sketch.png">
				<br>
				Click on the Groove icon:
				<br>
				<img style="padding: 10px 0px;" src="Tools/Tools_Images/Groove/groove_icon.gif">
				<br>
				Select the profile as the sketch and then select the axis of rotation; this can be the vertical axis in our case. If necessary, select the angle limits to restrict the rotation to a determined sector. You can also try experimenting with the other options (such as Thick Profile) to see what they do:
				<br>
				<img style="padding: 10px 0px;" src="Tools/Tools_Images/Groove/groove_box.png">
				<br>
				Click OK when done:
				<br>
				<img style="padding: 10px 0px;" src="Tools/Tools_Images/Groove/groove_result.png">
			</p>
