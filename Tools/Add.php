			<h1>Add Tool</h1>
			<p>
			<b>
				The Add tool is used to combine multiple bodies in a part into one body.
			</b>
				<br>
				Click on the Add tool.
				<br>
				<img src="Tools/Tools_Images/Add/add_icon.png">
				<br>
				A pop up box will appear. Select the secondary body as the 'Add' part and the primary body as the 'To' part.
				<br>
				<img src="Tools/Tools_Images/Add/add_parts.png">
				<br>
				Click OK, the intersecting objects should merge. You will notice that the bodies have merged into 1 Part Body in the tree, and “Add” appears on a branch of the tree.
				<br>
				<img src="Tools/Tools_Images/Add/add_product.png">
			</p>
